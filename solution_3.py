import math


def solve():
    try:
        line = input().strip()
        if not line:
            return
        n = int(line)
    except ValueError:
        return

    if n == 0:
        print(0)
        return

    # 1. Знаходимо базовий розмір k
    k = int(n ** (1 / 3))

    # Через похибки float перевіряємо, чи не є (k+1) кращим кандидатом, або чи k не завеликий
    # Але для n <= 2*10^9, int(n**(1/3)) зазвичай працює коректно.
    # На всяк випадок уточнимо k:
    while (k + 1) ** 3 <= n:
        k += 1
    while k ** 3 > n:
        k -= 1

    # 2. Визначаємо базові розміри блоку (a, b, c)
    # Варіанти об'ємів повних блоків:
    # V1 = k*k*k
    # V2 = k*k*(k+1)
    # V3 = k*(k+1)*(k+1)

    val1 = k ** 3
    val2 = k * k * (k + 1)
    val3 = k * (k + 1) ** 2

    if n <= val2:
        a, b, c = k, k, k
        rem = n - val1
    elif n <= val3:
        a, b, c = k, k, k + 1
        rem = n - val2
    else:
        a, b, c = k, k + 1, k + 1
        rem = n - val3

    # 3. Рахуємо вартість повного базового блоку
    # Формула: сума ребер по трьох осях
    base_matches = (a * (b + 1) * (c + 1) +
                    b * (a + 1) * (c + 1) +
                    c * (a + 1) * (b + 1))

    if rem == 0:
        print(base_matches)
        return

    # 4. Рахуємо вартість залишку (rem)
    # Залишок викладаємо на грань розміром a * b (або a * c, вони відсортовані, тому найменші)
    # Оптимально викласти залишок у формі прямокутника s * t

    s = int(math.isqrt(rem))
    if s == 0:
        t = 0
    else:
        t = rem // s

    left = rem - s * t  # "Хвостик", який не вліз у прямокутник s*t

    # Вартість додавання прямокутника s * t на існуючу поверхню:
    # Це додає (s+1)*(t+1) нових вертикальних стовпчиків (вершин)
    # Та s*(t+1) + t*(s+1) нових горизонтальних ребер ("стеля")
    added_matches = (s + 1) * (t + 1) + s * (t + 1) + t * (s + 1)

    # Якщо є хвостик (left), він додається збоку
    if left > 0:
        # Хвостик додає 3 сірника за кожен кубик + 2 замикаючих?
        # Точна формула додавання L кубиків в рядок до існуючої структури: 3*L + 2
        added_matches += 3 * left + 2

    print(base_matches + added_matches)


if __name__ == '__main__':
    solve()